import UIKit

extension UIColor {
    static func mainBackgroundColor() -> UIColor {
        return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    static func mainBorderColor() -> UIColor {
        return #colorLiteral(red: 0.8784313725, green: 0.8784313725, blue: 0.8784313725, alpha: 1)
    }
    
    static func mainCellsColor() -> UIColor {
        return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    static func mainTabColor() -> UIColor {
        return #colorLiteral(red: 0.1717679203, green: 0.1764954925, blue: 0.1851210892, alpha: 1)
    }
}
